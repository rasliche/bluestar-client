        <h2 class="pt-4">The Blue Star Difference</h2>
        <p class="pt-4">
          By taking this course, you have already committed to preserving the livelihood of the Florida Keys marine ecosystem. And while an environmental briefing aboard a dive or snorkel charter may seem like a small step, multiple studies show just how effective Blue Star Operators are at protecting the Florida Keys reefs.
        </p>
        <p class="pt-4">
          In 2012, researchers observed divers from Blue Star operated and non-Blue Star operated dive charters in Key Largo. One study found that nearly all divers (97%) interacted with the reef during their dive (Douglas and Camp, 2012). In the other study, researchers calculated that most recreational divers contact live stony coral approximately 18 times per dive, usually with fins, hands, or dangling dive gear (Krieger and Chadwick, 2012).      </p>
        <p class="pt-4">
          However, non-Blue Star divers were over two times more likely to interact with the reef than divers from Blue Star operations <cite>(Douglas and Camp, 2012)</cite>. The same trend was found for coral touches (Douglas and Camp, 2012, Krieger and Chadwick, 2012). Non-Blue Star divers deposited approximately twice the amount of sediment onto live corals than Blue Star divers (Krieger and Chadwick, 2012).
        </p>
        <p class="pt-4">
          The studies concluded that receiving a higher level of conservation education did influence diver behavior in the water and reduced the frequency of reef interactions (Douglas and Camp, 2012).
